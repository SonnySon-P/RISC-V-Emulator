    .text
    .globl _start

_start:
    # 輸入第一個數字
    lui a7, 0              # 將系統呼叫碼的高 20 位載入到 a7 (這裡 5 的高 20 位是 0)
    addi a7, a7, 5         # 載入低 12 位，將 a7 設置為 5
    ecall                  # 呼叫 ecall 讀取整數，結果存在 a0
    addi t0, a0, 0         # 將輸入的數字存到 t0 (a0 是讀入的數字)

    # 輸入第二個數字
    lui a7, 0              # 將系統呼叫碼的高 20 位載入到 a7 (這裡 5 的高 20 位是 0)
    addi a7, a7, 5         # 載入低 12 位，將 a7 設置為 5
    ecall                  # 呼叫 ecall 讀取整數，結果存在 a0
    addi t1, a0, 0         # 將第二個數字存到 t1

    # 計算加法
    add t2, t0, t1         # t2 = t0 + t1 (計算兩數字的總和)

    # 輸出結果
    lui a7, 0              # 將系統呼叫碼的高 20 位載入到 a7 (這裡 1 的高 20 位是 0)
    addi a7, a7, 1         # 載入低 12 位，將 a7 設置為 1 (輸出整數)
    addi a0, t2, 0         # 將結果放入 a0
    ecall                  # 呼叫 ecall 輸出整數

    # 結束程式
    lui a7, 0              # 將系統呼叫碼的高 20 位載入到 a7 (這裡 10 的高 20 位是 0)
    addi a7, a7, 10        # 載入低 12 位，將 a7 設置為 10 (結束程式)
    ecall                  # 呼叫 ecall 結束程式